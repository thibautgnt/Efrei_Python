"""
A magician has picked a secret number. He has hidden it in a variable named secret_number. He wants everyone who run his program to guess what number he has picked.
Your task is to help the magician complete the code in such a way so that the code:
will ask the user to enter an integer number;
will use a while loop;
If the number chosen by the user is different than the magician's secret number, the user should see the message "Ha ha! You're stuck in my loop!" and be prompted to enter a number again. If the number entered by the user matches the number picked by the magician, the number should be printed to the screen, and the magician should say the following words: "Well done, muggle! You are free now.Â«
"""

secret_number = 777
number = int(input("Enter a number: "))
while number != secret_number:
    print("Ha ha! You're stuck in my loop!")
    number = int(input("Enter a number: "))
print("Well done, muggle! You are free now.")